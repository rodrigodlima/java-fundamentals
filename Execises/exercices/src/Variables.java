public class Variables {
    public static void main(String[] args) {
        int myNumber = 10;
        double myTotal = 7.0;
        char myChar = 'a';
        boolean myBool = true;
        String myString = "teste";

        // Print vars
        System.out.println("Var int is: " + myNumber);
        System.out.println("Var double is: " + myTotal);
        System.out.println("Var char is: " + myChar);
        System.out.println("Var boolean is: " + myBool);
        System.out.println("Var string is: " + myString);
    }
}
