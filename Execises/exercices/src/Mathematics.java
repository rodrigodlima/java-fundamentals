public class Mathematics {
    public static void main(String[] args) {
        float numberOne = 20;
        float numberTwo = 10;

        // Addition
        float totalSoma = numberOne + numberTwo;
        System.out.println(totalSoma);

        // Subtraction
        float totalSubstracao = numberOne - numberTwo;
        System.out.println(totalSubstracao);

        // Multiplication
        float totalMulti = numberOne * numberTwo;
        System.out.println(totalMulti);

        // Division
        float totalDiv = numberOne / numberTwo;
        System.out.println(totalDiv);
    }
}
